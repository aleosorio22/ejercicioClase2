package umg.progra2.grupo1;

public class clsGrupo1 {
    public double areaCirculo(double radio) {
        return Math.PI * Math.pow(radio, 2);
    }

    // Método para calcular la circunferencia de un círculo
    public double circunferenciaCirculo(double radio) {
        return 2 * Math.PI * radio;
    }

    // Método para calcular el volumen de un cubo
    public double volumenCubo(double lado) {
        return Math.pow(lado, 3);
    }
}
