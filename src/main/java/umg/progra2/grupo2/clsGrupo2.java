package umg.progra2.grupo2;

public class clsGrupo2 {
    // Método para calcular el área de una pirámide cuadrada
    public double areaPiramideCuadrada(double base, double altura) {
        return (base * altura) / 2;
    }

    // Método para calcular el volumen de una pirámide cuadrada
    public double volumenPiramideCuadrada(double base, double altura) {
        return (base * altura) / 3;
    }

    // Método para calcular el área de un triángulo
    public double areaTriangulo(double base, double altura) {
        return (base * altura) / 2;
    }

}
